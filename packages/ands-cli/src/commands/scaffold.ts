// ---------------------------------------------------------------------------
// Command implementation
// ---------------------------------------------------------------------------

export async function runScaffold(options: ScaffoldOptions): Promise<number> {
  const { outputDir, pattern, featureName, force = false } = options;

  // Validate pattern
  const supportedPatterns = ['editable-form'];
  if (!supportedPatterns.includes(pattern)) {
    return emitOutput(
      makeOutput(
        'scaffold',
        false,
        ExitCode.ContractRuleFailure,
        `Unknown pattern: "${pattern}"`,
        [
          {
            category: 'scaffold',
            code: 'UNKNOWN_PATTERN',
            message: `Pattern "${pattern}" is not registered`,
            hint: `Supported patterns: ${supportedPatterns.join(', ')}`,
          },
        ],
      ),
    );
  }

  // Check output directory
  if (existsSync(outputDir) && !force) {
    return emitOutput(
      makeOutput(
        'scaffold',
        false,
        ExitCode.ContractRuleFailure,
        `Output directory already exists: ${outputDir}`,
        [
          {
            category: 'scaffold',
            code: 'OUTPUT_DIR_EXISTS',
            message: `Directory "${outputDir}" already exists`,
            hint: 'Use --force to overwrite existing files.',
          },
        ],
      ),
    );
  }

  // Generate files
  const filesToWrite: Array<{ filename: string; content: string }> = [];

  switch (pattern) {
    case 'editable-form': {
      filesToWrite.push(
        { filename: 'intent.js', content: generateIntentFile(options) },
        { filename: 'reducer.ts', content: generateReducerFile(options) },
        { filename: 'styles.css', content: generateStylesFile(options) },
        { filename: 'index.ts', content: generateIndexFile(options) },
      );
      break;
    }
    default: {
      // This is unreachable given the check above, but satisfies exhaustiveness
      const issues: Issue[] = [{
        category: 'internal',
        code: 'UNREACHABLE',
        message: 'Reached unreachable branch in scaffold switch',
      }];
      return emitOutput(makeOutput('scaffold', false, ExitCode.InternalError, 'Internal error', issues));
    }
  }

  // Write files
  try {
    mkdirSync(outputDir, { recursive: true });
    const written: string[] = [];
    for (const { filename, content } of filesToWrite) {
      const filePath = join(outputDir, filename);
      writeFileSync(filePath, content);
      written.push(filename);
    }

    return emitOutput(
      makeOutput(
        'scaffold',
        true,
        ExitCode.Success,
        `Scaffolded ${pattern} feature in ${outputDir}`,
        [],
        {
          data: {
            pattern,
            outputDir,
            filesWritten: written,
            nextStep: `ands validate ${join(outputDir, 'intent.js')}`,
          },
        },
      ),
    );
  } catch (e) {
    return emitOutput(
      makeOutput(
        'scaffold',
        false,
        ExitCode.InternalError,
        `Failed to write files: ${String(e instanceof Error ? e.message : e)}`,
        [
          {
            category: 'internal',
            code: 'FILE_WRITE_FAILURE',
            message: String(e instanceof Error ? e.message : e),
          },
        ],
      ),
    );
  }
}

// ---------------------------------------------------------------------------
// CLI argument parser
// ---------------------------------------------------------------------------

export interface ScaffoldArgs {
  pattern?: string;
  outputDir?: string;
  featureName?: string;
  adapterPackage?: string;
  force?: boolean;
}

export function parseScaffoldArgs(args: string[]): ScaffoldArgs {
  const result: ScaffoldArgs = {};
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--pattern' && args[i + 1]) {
      result.pattern = args[i + 1];
      i++;
    } else if (args[i] === '--output' && args[i + 1]) {
      result.outputDir = args[i + 1];
      i++;
    } else if (args[i] === '--name' && args[i + 1]) {
      result.featureName = args[i + 1];
      i++;
    } else if (args[i] === '--adapter' && args[i + 1]) {
      result.adapterPackage = args[i + 1];
      i++;
    } else if (args[i] === '--force') {
      result.force = true;
    }
  }
  return result;
}

