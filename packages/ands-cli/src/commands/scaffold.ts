/**
 * @module commands/scaffold
 * @description `ands scaffold --pattern <type> --output <dir>` — generates feature boilerplate.
 *
 * Generates:
 *   - `intent.js`     — intent object matching the pattern schema
 *   - `reducer.ts`    — state machine with exhaustive switch
 *   - `styles.css`    — CSS stub using token variables
 *   - `index.ts`      — barrel export
 *
 * Templates use token CSS variables and import from the adapter package
 * so the generated code is already compliant with ANDS conventions.
 *
 * **Output:** JSON to stdout. Files are written to `--output` directory.
 */

import { writeFileSync, mkdirSync, existsSync } from 'fs';
import { join } from 'path';
import { ExitCode } from '../exit-codes.js';
import { makeOutput, emitOutput } from '../output.js';
import type { Issue } from '../output.js';

// ---------------------------------------------------------------------------
// Scaffold options
// ---------------------------------------------------------------------------

export interface ScaffoldOptions {
  /** Pattern type to scaffold. Currently: 'editable-form'. */
  pattern: string;
  /** Output directory path. */
  outputDir: string;
  /**
   * Feature name used in generated identifiers.
   * E.g. 'user-profile' → generates 'user-profile-form' intent ID.
   */
  featureName: string;
  /**
   * Adapter package to import from.
   * @default '@ands/ds-adapter-example'
   */
  adapterPackage?: string;
  /** Overwrite existing files. @default false */
  force?: boolean;
}

